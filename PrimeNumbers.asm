MOV R0, #0X2F

MOV A, R0
MOV 0X10, #0X02
MOV B, #0X02
DIV AB
MOV 0X11, A
MOV A, R0

IF:
JZ COMPOSITE


ELSEIF:
MOV B, 0X10
DIV AB
MOV A, B
JZ COMPOSITE
MOV A, 0X11
SUBB A, 0X10
INC 0X10
JZ PRIME
MOV A, R0
SJMP ELSEIF


PRIME:
MOV R1,#0X01
JMP END

COMPOSITE:
MOV R1,#0X00

END: